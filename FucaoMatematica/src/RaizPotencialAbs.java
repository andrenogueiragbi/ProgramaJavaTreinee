
public class RaizPotencialAbs {

	public static void main(String[] args) {
double x = 2,y = 9, z = 25;
		
		System.out.println("RAIZ QUADRADA");
		x = Math.sqrt(x);
		y = Math.sqrt(y);
		z = Math.sqrt(z);	
		System.out.println(x);
		System.out.println(y);
		System.out.println(z+10);
		
		System.out.println("\nPOTENCIAÇÂO");
		x = Math.pow(2,4);
		y = Math.pow(y,6);
		z = Math.pow(z,8);	
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);		
		
		System.out.println("\nRAIZ ABSOLUTO");
		x = Math.abs(-5);
		y = Math.abs(5);
		z = Math.abs(-0.6);	
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);		
		
		System.out.println("ASS: ANDRE");
	}

}
